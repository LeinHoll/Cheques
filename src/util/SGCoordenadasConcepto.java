package util;

public class SGCoordenadasConcepto {
	int y, xCuenta, xConcepto, xIngreso, xEgreso;
	
	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getxCuenta() {
		return xCuenta;
	}

	public void setxCuenta(int xCuenta) {
		this.xCuenta = xCuenta;
	}

	public int getxConcepto() {
		return xConcepto;
	}

	public void setxConcepto(int xConcepto) {
		this.xConcepto = xConcepto;
	}

	public int getxIngreso() {
		return xIngreso;
	}

	public void setxIngreso(int xIngreso) {
		this.xIngreso = xIngreso;
	}

	public int getxEgreso() {
		return xEgreso;
	}

	public void setxEgreso(int xEgreso) {
		this.xEgreso = xEgreso;
	}
	
}
